package lab7;

public class Kisi {

	String ad;
	String soyad;
	int yas;
	
	public Kisi() {}

	public Kisi(String ad, String soyad, int yas) {
		this.ad = ad;
		this.soyad = soyad;
		this.yas = yas;
	}

	
	public String getAd() {
		return ad;
	}

	public String getSoyad() {
		return soyad;
	}

	public int getYas() {
		return yas;
	}

}
